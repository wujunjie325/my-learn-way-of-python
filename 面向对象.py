from time import sleep
class Clock(object):
    def __init__(self, s = 0, m = 0, h = 0):
        self._h = h
        self._s = s
        self._m = m
    def run(self):
        self._s += 1
        if self._s == 60:
            self._s = 0
            self._m += 1
            if self._m == 60:
                self._m = 0
                self._h += 1
    def show(self):
        return "%02d:%02d:%02d" % (self._h, self._m, self._s)

def main():
    clock = Clock(0, 10, 18)
    while True:
        print(clock.show())
        sleep(1)
        clock.run()
