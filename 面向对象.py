from math import sqrt
class Point(object):
    def __init__(self,x = 0,y = 0):
        self.x = x
        self.y = y
    def moveto(self, x, y):
        self.x = x
        self.y = y
    def moveby(self, x, y):
        self.x += x
        self.y += y
    def distance_to(self,other):
        dx = self.x - other.x
        dy = self.y - other.y
        return sqrt(dx**2 + dy**2)
    def __str__(self):
        return '(%s, %s)' % (str(self.x), str(self.y))
